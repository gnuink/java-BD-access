

 
public class deepartamento {
   
private int Coddep;
private String Nombre  ;

    public deepartamento() {
    }

    public deepartamento(int Coddep, String Nombre) {
        this.Coddep = Coddep;
        this.Nombre = Nombre;

    }
    public int getCoddep() {
        return Coddep;
    }
    public void setCoddep(int Coddep) {
        this.Coddep = Coddep;
    }
    public String getNombre() {
        return Nombre;
    }
    public void setNombre(String Nombre) {
        this.Nombre = Nombre;
    }


    
    
}
